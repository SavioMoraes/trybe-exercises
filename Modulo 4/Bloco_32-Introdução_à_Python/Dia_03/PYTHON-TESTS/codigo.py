def is_even(number):
    "Retorna True se um número é par, senão False."
    return number % 2 == 0


def is_odd(number):
    "Retorna True se um número é ímpar, senão False."
    return number % 2 != 0


def divide(numerator, denominator):
    "Retorna a divisão de numerator por denominator"
    return numerator / denominator
