// Adição Função
function sum(a, b) {
  return a + b;
}
console.log(sum(3, 8));

// Subtração Função
function subtraction(a, b) {
  return a - b;
}
console.log(subtraction(200, 120));

// Multiplicação Função
function multiplication(a, b) {
  return a * b;
}
console.log(multiplication(8, 5));

// Divisão Função
function division(a, b) {
  return a / b;
}
console.log(division(81, 9));

//Módulo Função
function module(a, b) {
  return a % b;
}
console.log(module(4, 2));